#🧭 What Are Control Statements?
#Control statements help your program decide what to do next. They include:

#Conditional statements – like “if this happens, do that”

#Loops – like “do this again and again”

#Jump statements – like “stop here” or “skip this part”

#🔍 1. Conditional Statements (if, elif, else)
#These let your code make decisions.
#Example:
age = 18

if age >= 18:
    print("You're an adult!")
elif age >= 13:
    print("You're a teenager!")
else:
    print("You're a child!")
#🧠 How it works:
#if checks the first condition

#elif means “else if” — another condition

#else runs if nothing above was true